#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = (char*)malloc(n + 1);
	if (!s2)
		return (NULL);
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
